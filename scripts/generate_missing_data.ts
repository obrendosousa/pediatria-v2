import * as fs from 'fs';
import * as path from 'path';

const STANDARDS_DIR = path.join(process.cwd(), 'data', 'standards');

// ═══ WHO 2007 ═══════════════════════════════════════════════════════════════

function generateWHO2007Files(): void {
  const who2007Path = path.join(process.cwd(), 'data', 'who2007', 'who2007_growth_reference_5_19.json');
  if (!fs.existsSync(who2007Path)) {
    console.error('❌ Arquivo WHO 2007 não encontrado:', who2007Path);
    return;
  }
  const who2007 = JSON.parse(fs.readFileSync(who2007Path, 'utf-8'));

  const indicatorMap: Record<string, { filePrefix: string; ageEnd: number }> = {
    weightForAge:  { filePrefix: 'wfa',   ageEnd: 120 },
    heightForAge:  { filePrefix: 'lhfa',  ageEnd: 228 },
    bmiForAge:     { filePrefix: 'bmifa', ageEnd: 228 },
  };

  const genderMap: Record<string, string> = { boys: 'boys', girls: 'girls' };

  for (const [indicator, config] of Object.entries(indicatorMap)) {
    const indData = who2007.indicators?.[indicator]?.data;
    if (!indData) {
      console.warn(`⚠️ Dados não encontrados para ${indicator}`);
      continue;
    }

    for (const [genderKey, genderLabel] of Object.entries(genderMap)) {
      const rows = indData[genderKey];
      if (!rows?.length) {
        console.warn(`⚠️ Sem dados para ${indicator}/${genderKey}`);
        continue;
      }

      // Determinar age_range pelo indicador
      const ageRange = config.filePrefix === 'wfa' ? '5_10' : '5_19';
      const filename = `${config.filePrefix}_${genderLabel}_${ageRange}_zscores.json`;

      // Filtrar apenas meses dentro da faixa (61+ para garantir dados 5-19)
      interface WHO2007Row { month: number; L: number; M: number; S: number; SD3neg: number; SD2neg: number; SD1neg: number; SD0: number; SD1: number; SD2: number; SD3: number }
      const filteredRows = (rows as WHO2007Row[])
        .filter((r) => r.month >= 61 && r.month <= config.ageEnd)
        .map((r) => ({
          Month: r.month,
          L: r.L,
          M: r.M,
          S: r.S,
          SD3neg: r.SD3neg,
          SD2neg: r.SD2neg,
          SD1neg: r.SD1neg,
          SD0: r.SD0,
          SD1: r.SD1,
          SD2: r.SD2,
          SD3: r.SD3,
        }));

      const outPath = path.join(STANDARDS_DIR, filename);
      fs.writeFileSync(outPath, JSON.stringify(filteredRows, null, 2));
      console.log(`✅ WHO 2007: ${filename} (${filteredRows.length} linhas)`);
    }
  }
}

// ═══ UNICAMP — BERTAPELLI et al. 2017 ═══════════════════════════════════════
// Dados extraídos de: Bertapelli F, et al. J Epidemiol 2017;27(6):265-273 (PMC5463025)
// e Bertapelli F, et al. J Pediatr 2017;93(1):94-99 (BMI com LMS)
// via sdcurve GitHub: https://github.com/VWApplications/sdcurve

// Fórmula LMS: X = M*(1+L*S*z)^(1/L) — calcular z-scores a partir dos percentis
// Para dados sem LMS, aproximamos SD com: SD ≈ (P97-P3)/(2*1.88079)

interface PercentileRow {
  month: number;
  P3: number; P10: number; P25: number; P50: number; P75: number; P90: number; P97: number;
}

interface LMSBMIRow {
  month: number;
  L: number; M: number; S: number;
  P5: number; P10: number; P25: number; P50: number; P75: number; P85: number; P90: number; P95: number;
}

// Calcula z-scores aproximados a partir dos percentis (sem LMS)
function percentilesToZScores(row: PercentileRow) {
  // Usar os percentis diretamente para calcular SDs (assimétrico)
  const sdWidthNeg = (row.P50 - row.P3) / 1.88079;
  const sdWidthPos = (row.P97 - row.P50) / 1.88079;

  return {
    Month: row.month,
    L: 1, // Aproximação: distribuição normal (L=1)
    M: round(row.P50),
    S: round((row.P97 - row.P3) / (2 * 1.88079 * row.P50), 5),
    SD3neg: round(row.P50 - 3 * sdWidthNeg),
    SD2neg: round(row.P50 - 2 * sdWidthNeg),
    SD1neg: round(row.P50 - 1 * sdWidthNeg),
    SD0: round(row.P50),
    SD1: round(row.P50 + 1 * sdWidthPos),
    SD2: round(row.P50 + 2 * sdWidthPos),
    SD3: round(row.P50 + 3 * sdWidthPos),
    P3: round(row.P3),
    P15: round(row.P10 + (row.P25 - row.P10) * 0.3), // Interpolar P15 de P10 e P25
    P50: round(row.P50),
    P85: round(row.P75 + (row.P90 - row.P75) * 0.67), // Interpolar P85 de P75 e P90
    P97: round(row.P97),
  };
}

function lmsBMIToRow(row: LMSBMIRow) {
  const { L, M, S } = row;
  const calcZ = (z: number) => {
    if (Math.abs(L) < 0.001) return M * Math.exp(S * z);
    return M * Math.pow(1 + L * S * z, 1 / L);
  };
  return {
    Month: row.month,
    L: row.L,
    M: round(row.M),
    S: row.S,
    SD3neg: round(calcZ(-3)),
    SD2neg: round(calcZ(-2)),
    SD1neg: round(calcZ(-1)),
    SD0: round(calcZ(0)),
    SD1: round(calcZ(1)),
    SD2: round(calcZ(2)),
    SD3: round(calcZ(3)),
    P3: round(calcZ(-1.88079)),
    P15: round(calcZ(-1.03643)),
    P50: round(calcZ(0)),
    P85: round(calcZ(1.03643)),
    P97: round(calcZ(1.88079)),
  };
}

function round(v: number, decimals = 1): number {
  const f = Math.pow(10, decimals);
  return Math.round(v * f) / f;
}

